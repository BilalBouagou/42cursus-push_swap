#include "libft.h"

size_t	ft_intstrlen(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] == '0')
		i++;
	return (ft_strlen(&str[i]));
}
